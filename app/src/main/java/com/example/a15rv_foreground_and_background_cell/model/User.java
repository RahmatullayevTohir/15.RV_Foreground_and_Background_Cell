package com.example.a15rv_foreground_and_background_cell.model;

public class User {
    public String name;
    public String location;

    public User(String name, String location) {
        this.name = name;
        this.location = location;
    }

    public String getName() {
        return name;
    }

    public String getLocation() {
        return location;
    }
}
